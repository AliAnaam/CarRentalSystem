    private void refreshTable() {
    tableModel.setRowCount(0);
    for (Customer c : dm.getCustomers()) {
        tableModel.addRow(new Object[]{
            c.getId(),
            c.getFullName(),
            c.getPhone(),
            c.getEmail(),
            c.getLicenseNumber()
        });
    }
}
    
